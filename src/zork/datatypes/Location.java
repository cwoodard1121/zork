package datatypes;

public class Location {
    private int x;
    private int y;

    public Location(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public double distanceTo(Location loc) {
        return Math.sqrt(Math.pow((x - loc.getX()),2) + Math.pow((y - loc.y),2));
    }
    
}
